#include <iostream>


using namespace std;



int main() {

    // Declare variables to store day, month, and year

    int day, month, year;


    
    // Prompt the user to enter values for day, month, and year
    
    cout << "Enter day: ";
    cin >> day;


    
    cout << "Enter month: ";
    
    cin >> month;


    
    cout << "Enter year: ";
    
    cin >> year;


    
    // Print out the values entered by the user
    
    cout << "Day: " << day << endl;
    
    cout << "Month: " << month << endl;
    
    cout << "Year: " << year << endl;




    return 0;
}
